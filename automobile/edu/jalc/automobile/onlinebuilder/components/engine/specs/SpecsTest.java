package edu.jalc.automobile.onlinebuilder.components.engine.specs;

public class SpecsTest{
  public static void main(String... args){
    System.out.print("testing specs");
    HorsePower hp = new HorsePower(0,0);
    Torque tq = new Torque(0,0);
    hp.setHorsePower(655);
    hp.setRPM(4200);
    tq.setTorque(480);
    tq.setRPM(2900);
    assert(hp.getHorsePower() == 655);
    assert(hp.getRPM() == 4200);
    assert(tq.getTorque() == 480);
    assert(tq.getRPM() == 2900);
    System.out.println(" - all tests passed");
  }
}